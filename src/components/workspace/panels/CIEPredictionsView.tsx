// ============================================================================
// HELPER: Get all predictions with client info
// ============================================================================

interface PredictionWithClient extends Prediction {
  clientName: string
  clientId: string
}

function getAllPredictionsWithClients(cieClients: CIEClient[]): PredictionWithClient[] {
  const predictions = cieClients
    .filter(c => c.prediction)
    .map(c => c.prediction!)

  return predictions.map(pred => {
    const client = cieClients.find(c => c.client.id === pred.clientId)
    return {
      ...pred,
      clientName: client?.client.name || 'Unknown',
    }
  })
}

// ============================================================================
// CONFIDENCE RING SVG COMPONENT
// ============================================================================

function ConfidenceRing({ confidence, size = 24 }: { confidence: number; size?: number }) {
  const r = (size - 3) / 2
  const circ = 2 * Math.PI * r
  const offset = circ - (confidence / 100) * circ
  const color = confidence >= 80 ? '#22c55e' : confidence >= 60 ? '#f59e0b' : '#ef4444'

  return (
    <svg width={size} height={size} className="flex-shrink-0">
      <circle cx={size / 2} cy={size / 2} r={r} fill="none" stroke="rgba(255,255,255,0.1)" strokeWidth="2" />
      <circle
        cx={size / 2}
        cy={size / 2}
        r={r}
        fill="none"
        stroke={color}
        strokeWidth="2"
        strokeDasharray={circ}
        strokeDashoffset={offset}
        strokeLinecap="round"
        transform={`rotate(-90 ${size / 2} ${size / 2})`}
      />
      <text
        x={size / 2}
        y={size / 2}
        textAnchor="middle"
        dominantBaseline="central"
        fill={color}
        fontSize="8"
        fontWeight="bold"
        fontFamily="monospace"
      >
        {confidence}
      </text>
    </svg>
  )
}

