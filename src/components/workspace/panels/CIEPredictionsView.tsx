        summary: `${name} is showing a pattern of extended deliberation without progression toward a decision. The CIE engine has detected an unusually high volume of comparative behaviour paired with stagnant commitment indicators, suggesting cognitive overload is impeding forward movement.`,
        howCreated: `The engine identified this pattern by tracking the ratio of information-gathering actions to decision-stage actions over a rolling 30-day window. ${name}'s viewing patterns show repetitive cycling between the same shortlisted options without narrowing. Inquiry data reveals questions being asked multiple times in different ways, a hallmark of uncertainty. The Analytical Rigour dimension is scoring abnormally high while Decision Velocity has declined significantly.`,
