export default function CIEPredictionsView() {
  const [selectedPattern, setSelectedPattern] = useState<PredictionPattern | null>(null)
  const [expandedPrediction, setExpandedPrediction] = useState<PredictionWithClient | null>(null)

  const { data: liveCIEClients, loading } = useCIEClients()
  const cieClients = liveCIEClients || []

  // Loading guard AFTER all hook calls
  if (loading && cieClients.length === 0) return <P1Loading message="Loading CIE data..." />

  // Get all predictions with client data
  const allPredictionsWithClients = useMemo(
    () => getAllPredictionsWithClients(cieClients),
    [cieClients]
  )

  // Count predictions per pattern
  const patternCounts = useMemo(() => {
    const counts: Record<PredictionPattern, number> = {
      'Imminent Purchase': 0,
      'Analysis Paralysis': 0,
      'Price Negotiation': 0,
      'Trust Erosion': 0,
      'Portfolio Expansion': 0,
      'Flight Risk': 0,
      'Upsell Opportunity': 0,
    }
    allPredictionsWithClients.forEach(p => {
      counts[p.pattern]++
    })
    return counts
  }, [allPredictionsWithClients])

  // Total active predictions
  const totalActive = allPredictionsWithClients.length

  // Filter predictions by selected pattern and sort by confidence descending
  const filteredAndSorted = useMemo(() => {
    let filtered = allPredictionsWithClients
    if (selectedPattern) {
      filtered = filtered.filter(p => p.pattern === selectedPattern)
    }
    return filtered.sort((a, b) => b.confidence - a.confidence)
  }, [allPredictionsWithClients, selectedPattern])

  // High-confidence alerts (>= 80)
  const highConfidenceAlerts = useMemo(
    () => filteredAndSorted.filter(p => p.confidence >= 80),
    [filteredAndSorted]
  )

  // Get momentum symbol and color
  const getMomentumDisplay = (momentum: MomentumDirection) => {
    switch (momentum) {
      case 'heating':
        return { symbol: '\u25B2', color: 'text-red-400' }
      case 'cooling':
        return { symbol: '\u25BC', color: 'text-blue-400' }
      case 'stable':
        return { symbol: '\u2192', color: 'text-pcis-text-muted' }
    }
  }

  const patterns: PredictionPattern[] = [
    'Imminent Purchase',
    'Analysis Paralysis',
    'Price Negotiation',
    'Trust Erosion',
    'Portfolio Expansion',
    'Flight Risk',
    'Upsell Opportunity',
  ]

  // Unique client count
  const uniqueClientCount = new Set(allPredictionsWithClients.map(p => p.clientId)).size

  return (
    <div className="h-full flex flex-col overflow-y-auto">
      {/* Prediction Detail Overlay */}
      {expandedPrediction && (
        <PredictionDetailOverlay
          prediction={expandedPrediction}
          onClose={() => setExpandedPrediction(null)}
        />
      )}

      {/* ===== HEADER ===== */}
      <div className="px-4 py-3 border-b border-pcis-border/20">
        <h1 className="text-[13px] font-semibold text-white mb-1" style={{ fontFamily: "'Playfair Display', serif" }}>
          Prediction Engine
        </h1>
        <p className="text-[10px] text-pcis-text-secondary">
          {totalActive} active {totalActive === 1 ? 'prediction' : 'predictions'} across {uniqueClientCount} {uniqueClientCount === 1 ? 'client' : 'clients'}
        </p>
      </div>

      {/* ===== STATS BAR ===== */}
      <div className="px-4 py-3 border-b border-pcis-border/10 flex items-center gap-4 flex-wrap">
        <div className="flex items-center gap-1.5">
          <span className="text-[9px] text-pcis-text-muted">Total:</span>
          <span className="text-[10px] font-mono font-semibold text-pcis-gold">{totalActive}</span>
        </div>
        <div className="w-px h-3 bg-pcis-border/30" />
        <div className="flex items-center gap-1.5">
          <span className="text-[9px] text-pcis-text-muted">High Confidence:</span>
          <span className="text-[10px] font-mono font-semibold text-green-400">{highConfidenceAlerts.length}</span>
        </div>
        <div className="w-px h-3 bg-pcis-border/30" />
        {patterns.map(pattern => {
          const count = patternCounts[pattern]
          return count > 0 ? (
            <div key={pattern} className="flex items-center gap-1.5">
              <span className="text-[8px] text-pcis-text-muted uppercase">{pattern.split(' ')[0]}:</span>
              <span className="text-[9px] font-mono font-semibold" style={{ color: PATTERN_META[pattern].hex }}>
                {count}
              </span>
            </div>
          ) : null
        })}
      </div>

      {/* ===== PATTERN FILTER PILLS ===== */}
      <div className="px-4 py-3 border-b border-pcis-border/10 flex gap-2 flex-wrap">
        {patterns.map(pattern => {
          const count = patternCounts[pattern]
          const isSelected = selectedPattern === pattern
          const meta = PATTERN_META[pattern]

          return (
            <button
              key={pattern}
              onClick={() =>
                setSelectedPattern(selectedPattern === pattern ? null : pattern)
              }
              className={`text-[9px] px-2.5 py-1 rounded transition-all ${
                isSelected
                  ? `${meta.bgColor} ${meta.borderColor} border`
                  : `border border-pcis-border/20 bg-white/[0.01] hover:bg-white/[0.02]`
              }`}
              style={isSelected ? { color: meta.color.split('-')[1] ? undefined : meta.color } : {}}
            >
              {pattern} ({count})
            </button>
          )
        })}
      </div>

      {/* ===== SCROLLABLE CONTENT ===== */}
      <div className="flex-1 overflow-y-auto space-y-3 p-4">
        {/* HIGH-CONFIDENCE ALERTS (>= 80) */}
        {highConfidenceAlerts.length > 0 && (
          <Panel>
            <PanelHeader title="High-Confidence Alerts" accent="#f59e0b" right={
              <span className="text-[9px] text-pcis-text-muted font-mono">{highConfidenceAlerts.length}</span>
            } />
            <div className="divide-y divide-pcis-border/10 p-3 space-y-2">
              {highConfidenceAlerts.map((pred, i) => {
                const meta = PATTERN_META[pred.pattern]

                return (
                  <div
                    key={i}
                    className="flex items-start gap-3 pt-2 first:pt-0 cursor-pointer hover:bg-pcis-gold/[0.04] rounded-lg px-2 py-2 -mx-2 transition-colors group"
                    onClick={() => setExpandedPrediction(pred)}
                  >
                    <ConfidenceRing confidence={pred.confidence} />
                    <div className="flex-1 min-w-0">
                      <div className="flex items-center gap-2 mb-1">
                        <ClientLink clientId={pred.clientId} className="text-[10px] font-semibold text-white">
                          {pred.clientName}
                        </ClientLink>
                        <span className={`text-[8px] px-2 py-0.5 rounded border ${meta.borderColor} ${meta.color}`}>
                          {pred.pattern}
                        </span>
                      </div>
                      <p className="text-[9px] text-pcis-text-secondary">
                        {pred.description}
                      </p>
                      <span className="text-[8px] text-pcis-gold/0 group-hover:text-pcis-gold/60 transition-colors mt-1 inline-block">
                        VIEW ANALYSIS
                      </span>
                    </div>
                  </div>
                )
              })}
            </div>
          </Panel>
        )}

        {/* ALL PREDICTIONS LIST */}
        {filteredAndSorted.length > 0 ? (
          <Panel>
            <PanelHeader
              title={selectedPattern ? `${selectedPattern}` : 'All Predictions'}
              accent="#d4a574"
              right={
                <span className="text-[9px] text-pcis-text-muted font-mono">{filteredAndSorted.length}</span>
              }
            />
            <div className="divide-y divide-pcis-border/10 p-3">
              {filteredAndSorted.map((pred, i) => {
                const meta = PATTERN_META[pred.pattern]
                const momentum = getMomentumDisplay(pred.momentum)

                return (
                  <div
                    key={i}
                    className="py-2 first:pt-0 last:pb-0 hover:bg-pcis-gold/[0.04] transition-colors cursor-pointer rounded-lg px-2 -mx-2 group"
                    onClick={() => setExpandedPrediction(pred)}
                  >
                    <div className="flex items-start gap-3">
                      <ConfidenceRing confidence={pred.confidence} />
                      <div className="flex-1 min-w-0">
                        <div className="flex items-center gap-2 mb-1">
                          <ClientLink clientId={pred.clientId} className="text-[10px] font-semibold text-white">
                            {pred.clientName}
                          </ClientLink>
                          <span className={`text-[8px] px-2 py-0.5 rounded border ${meta.borderColor} ${meta.color}`}>
                            {pred.pattern}
                          </span>
                        </div>
                        <p className="text-[9px] text-pcis-text-secondary mb-1">
                          {pred.description}
                        </p>
                        <span className="text-[8px] text-pcis-gold/0 group-hover:text-pcis-gold/60 transition-colors inline-block">
                          VIEW ANALYSIS
                        </span>
                      </div>
                      <div className="flex items-center gap-2 flex-shrink-0 ml-2">
                        <span className={`text-[11px] ${momentum.color}`}>
                          {momentum.symbol}
                        </span>
                        <span className="text-[8px] text-pcis-text-muted font-mono">
                          {pred.detectedAt}
                        </span>
                      </div>
                    </div>
                  </div>
                )
              })}
            </div>
          </Panel>
        ) : (
          <Panel>
            <div className="px-4 py-4 text-center">
              <p className="text-[10px] text-pcis-text-muted">
                {selectedPattern
                  ? `No predictions matching "${selectedPattern}"`
                  : 'No predictions available'}
              </p>
            </div>
          </Panel>
        )}

        {/* ===== PCISCOM INTEGRATION NOTE ===== */}
        <div className="text-[8px] text-pcis-text-muted italic px-4 pb-2">
          <span className="text-pcis-gold">&#9670;</span> PCISCOM will auto-feed WhatsApp conversation patterns into prediction signals
        </div>
      </div>
    </div>
  )
}
